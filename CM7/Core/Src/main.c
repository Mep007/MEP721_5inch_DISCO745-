void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)   // callbacks for EXT_INT
{
	//================================================================================
	 if (GPIO_Pin == INT_TOUCH_Pin)  // Touch INT callback
	 {
		 __NOP();
	 }

	 //================================================================================
	 else if (GPIO_Pin == BUT_USR_Pin)  // USER BUT (BLUE HAT) INT callback
	 {
		 myLCD.BL.target_old = myLCD.BL.target;   //save actual brightness
		 myLCD.BL.enable     = true;            //enable brightness change

		 if (!myLCD.BL.dir)  // decreasing brightness
		 {
		   if (myLCD.BL.target >= 50) { myLCD.BL.target -= 50; }
		   else  myLCD.BL.dir = 1;
		 }
		 else  // increasing brightness
		 {
			 if (myLCD.BL.target <= 450) { myLCD.BL.target  += 50; }
			 else  myLCD.BL.dir = 0;
		 }
	 }

}
