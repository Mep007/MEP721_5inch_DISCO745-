//--------------------------------------------------------------------------------------
PT_THREAD(LCD_SetBrightness_PT(struct pt *pt, uint16_t target_BL))  // change LCD BL PWM in non-blocking mode - need to call this PT periodicaly
{
   static uint16_t  out_BL=0;

   PT_BEGIN(pt);

   PT_WAIT_UNTIL(pt, myLCD.BL.enable == true);   // someone wants to set new backlight ?

   while (out_BL != target_BL)                  // Dokud není dosažen požadovaný jas
   {
	 if (out_BL < target_BL)		 out_BL++;                 // Pokud je třeba zvýšit jas
	 else if (out_BL > target_BL)    out_BL--;          // Pokud je třeba snížit jas

	 LCD_SetBrightness(out_BL);                 // Nastavení PWM
	 PT_YIELD(pt);                              // Vrátí řízení a čeká na další volání
   }

   myLCD.BL.enable = false;     // disable to set bl again, until someone wants to

   PT_END(pt);
}
