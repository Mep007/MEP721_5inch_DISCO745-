//--------------------------------------------------------------------------------------
void LCD_SetBrightness(uint16_t percent)  //  0-499 -> 0-100%
{
    uint32_t max_value = LCD_BL_PWM_TIM.Instance->ARR;  // read actual Timer Counter-period value (set via CubeMx)

    if (percent > 500) percent = 500;
    uint32_t pwm_value = (uint32_t)percent * (max_value / 500u);
    LCD_BL_PWM_TIM.Instance->CCR3 = pwm_value;     // PK0 PWM set to channel 3
}
